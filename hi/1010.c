#include <stdio.h>

int main()
{
    int n;
    scanf("%d", &n);

    // first parameter : a kind of variable
    // second parameter : a variable name
    // ex) scanf("%d, %d", &num1, &num2)

    // d : int
    // c : char
    // s : char*
    // lf : double
    // f : float

    // & : The address of the variable

    printf("%d", n);

    return 0;
}